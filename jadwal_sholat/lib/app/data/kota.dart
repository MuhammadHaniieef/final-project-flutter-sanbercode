// helper untuk dropdown kota (by prov code)
List<String> cityNamesOfProvCode(String provCode) {
  final list = kCitiesByProvCode[provCode];
  if (list == null) return const [];
  return [for (final c in list) c['nama'] ?? ''];
}

// ambil ID kota berdasarkan (provCode, cityName)
String? cityIdByProvAndName(String provCode, String cityName) {
  final list = kCitiesByProvCode[provCode];
  if (list == null) return null;
  for (final c in list) {
    if ((c['nama'] ?? '') == cityName) return c['id'];
  }
  return null;
}
